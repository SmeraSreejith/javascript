//  Data structure                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                   
// array
weekdays = ['mon','tue','wed','thu']
//display an array
console.log(weekdays);

//total number of items in an array
console.log(weekdays.length);

//print wed from the given array
console.log(weekdays[2/*index value*/]);

//print thu from the given array
console.log(weekdays[3]);

//add friday to a given array
//push - for adding
weekdays.push('fri')
console.log(weekdays);


//add 3 to the given array
weekdays.push(3)
console.log(weekdays);

//remove 3 from the given array
//pop - for removing
weekdays.pop()
console.log(weekdays);

//add sun to the given array
//unshift -add item to start
weekdays.unshift('sun')
console.log(weekdays);

weekdays.unshift('sat')
console.log(weekdays);


//shift-remove item from start
weekdays.shift()
console.log(weekdays);















